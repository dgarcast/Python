#Un string es una sucesion de caracteres que son un texto, no afecta en nada
#solo son palabras de texto

#Para separar dos texto dentro de un print, usamos el +
print("Hola " + "Mundo")
print("Hola" + " " + "Mundo")

#Podemos usar la barra invertida mas las comidas para decir a 
#python que tome la palabra que va entre barras que sea un caracter especial
print("Hola \"Mundo\"")

#Para poner dos lineas dentro de una misma ejecucion print usamos \n
print("Esto es una linea \nY esto es otra linea")

#Tambien podemos usar otro caracter para tabular \t
print("Esto es una linea\n\ty esto es otra")

#Si queremos escribir ingles debemos usar \ antes del apostrofe
print("This isn\'t my number")

#Si queremos poner una barra \ invertida en un print debemos duplicarla \\
print("Barra invertida \\")